package com.callndata.item;

public class MyFriendListItem {

	String num, Name, Number;

	public MyFriendListItem() {

	}

	public MyFriendListItem(String num, String Name, String Number) {
		this.num = num;
		this.Name = Name;
		this.Number = Number;
	}

	public String getNum() {
		return num;
	}

	public void setNum(String num) {
		this.num = num;
	}

	public String getName() {
		return Name;
	}

	public void setName(String name) {
		Name = name;
	}

	public String getNumber() {
		return Number;
	}

	public void setNumber(String number) {
		Number = number;
	}

}
